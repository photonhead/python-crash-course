# numbers.py file is to practice how to handle numbers.

# allocating numbers into three variables.
x, y, z = 1, 7, 8
print(f"x: {x}, y: {y}, z: {z}") # print three variables.

# set a calculation for each variables.
addition = x + y
subtraction = z - x
multiplication = x * z 
division = z / x
# print each variables to display results of the calculations.
print(f"addition: {addition}\nsubtraction: {subtraction}\nmultiplication: {multiplication}\ndivision: {division}")

# allocate my favorite numbers to a variable as a list.
favorite_numbers = [1, 3, 7, 8, 9]
message = "My favorite numbers are "f"{favorite_numbers}."
print(message) # print a message what my favorite numbers are in a list form.